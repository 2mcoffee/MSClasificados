	Usted no tiene que darnos ninguna información personal con objeto de realizar búsquedas en el Sitio Web.
	<br>
	En algunas áreas del Sitio Web, Mercados y Servicios requiere o puede requerir que Usted facilite datos personales, incluido su nombre, dirección, dirección de correo electrónico, número de teléfono, información de contacto, información para facturación y otros datos de los que se pueda deducir su identidad. 
