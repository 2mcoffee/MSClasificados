	<h2>5. Uso de contadores y cookies</h2>
	El sitio utiliza un sistema propio de estadisticas que nos permite contar los Usuarios que han visitado el Sitio Web. Los contadores se usan para acceder a su información personal identificable que se almacena en Mercados y Servicios, sino que constituyen una técnica que usamos para confeccionar estadísticas de forma colectiva sobre el uso de nuestro Sitio Web. 
	Usted no puede desactivar dicha funcionalidad.
	<br>
	<br>
	Mercados y Servicios utiliza cookies cuando un Usuario navega por los sitios y páginas del Sitio Web. 
	Nuestras cookies se asocian únicamente con un Usuario y su ordenador. 
	Gracias a las cookies, resulta posible que Mercados y Servicios reconozca a los Usuarios registrados después de que éstos se hayan registrado por primera vez, sin que se tengan que registrar en cada visita para acceder a las áreas y servicios reservados exclusivamente a ellos. 
