	Asimismo, Mercados y Servicios obtiene o puede obtener cierta información personal sobre el uso que hace del Sitio Web y el modo de acceso al mismo, como por ejemplo a qué páginas accede, dirección IP, tipo de navegador, nombres de dominio, horas de acceso y referencias a direcciones de sitios web, aunque esta información no queda vinculada a sus datos personales.
