	Mercados y Servicios cifra los datos identificativos del Usuario para mayor seguridad. 
	El Usuario tiene la posibilidad de configurar su navegador para ser avisado en pantalla de la recepción de cookies y para impedir la instalación de cookies en su disco duro. Por favor, consulte las instrucciones y manuales de su navegador para ampliar ésta información. 
	Para utilizar el Sitio Web, no resulta necesario que el Usuario permita la instalación de las cookies enviadas por Mercados y Servicios, sin perjuicio de que en tal caso será necesario que el Usuario se registre como Usuario de cada uno de los Servicios prestados en el Sitio Web.
